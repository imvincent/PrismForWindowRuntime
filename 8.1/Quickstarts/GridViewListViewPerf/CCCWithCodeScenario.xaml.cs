        {
            navigationHelper.OnNavigatedTo(e);
        }

        protected override void OnNavigatedFrom(NavigationEventArgs e)
        {
            navigationHelper.OnNavigatedFrom(e);
        }

        #endregion

        /// <summary>
        /// We will visualize the data item asynchronously in multiple phases for improved panning user experience 
        /// of large lists.  In this sample scenario, we will visualize different parts of the data item
        /// in the following order:
        /// 
        ///     1) Placeholders (visualized synchronously - Phase 0)
        ///     2) Title (visualized asynchronously - Phase 1)
        ///     3) Category and Image (visualized asynchronously - Phase 2)
        ///
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="args"></param>
        void ItemGridView_ContainerContentChanging(ListViewBase sender, ContainerContentChangingEventArgs args)
        {
            ItemViewer iv = args.ItemContainer.ContentTemplateRoot as ItemViewer;

            if (args.InRecycleQueue == true)
            {
                iv.ClearData();
            }
            else if (args.Phase == 0)
            {
                iv.ShowPlaceholder(args.Item as Item);

                // Register for async callback to visualize Title asynchronously
                args.RegisterUpdateCallback(ContainerContentChangingDelegate);
            }
            else if (args.Phase == 1)
            {
                iv.ShowTitle();
                args.RegisterUpdateCallback(ContainerContentChangingDelegate);
            }
            else if (args.Phase == 2)
            {
                iv.ShowCategory();
                iv.ShowImage();
                iv.ShowLinkbutton();
            }

            // For imporved performance, set Handled to true since app is visualizing the data item
            args.Handled = true;
        }

        /// <summary>
        /// Managing delegate creation to ensure we instantiate a single instance for 
        /// optimal performance. 
        /// </summary>
        private TypedEventHandler<ListViewBase, ContainerContentChangingEventArgs> ContainerContentChangingDelegate
        {
            get
            {
                if (_delegate == null)
                {
                    _delegate = new TypedEventHandler<ListViewBase, ContainerContentChangingEventArgs>(ItemGridView_ContainerContentChanging);
                }
                return _delegate;
            }
        }
        private TypedEventHandler<ListViewBase, ContainerContentChangingEventArgs> _delegate;

        private void ItemGridView_Loaded(object sender, RoutedEventArgs e)
        {
            // Find the ScrollViewer inside the GridView
            var scrollViewer = VisualTreeUtilities.GetVisualChild<ScrollViewer>(ItemGridView);
            scrollViewer.LayoutUpdated += scrollViewer_LayoutUpdated;
        }

        /// <summary>
        /// Stores the time before beginning the new layout.
        /// </summary>
        void scrollViewer_LayoutUpdated(object sender, object e)
        {
            lastLayoutUpdated = DateTime.Now;
        }

        /// <summary>
        /// Handles the Show Duration button and displays the approximate time
        /// span for the operation to complete.
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void ShowDuration_Click(object sender, RoutedEventArgs e)
        {
            var timespan = lastLayoutUpdated.Subtract(startTime);
            ItemsGridViewDuration.Text = timespan.TotalSeconds.ToString();
        }
    }
